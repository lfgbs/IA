class Cidades(SearchDomain):
    def __init__(self,connections, coordinates):
        self.connections = connections
        self.coordinates = coordinates
    def actions(self,city):
        actlist = []
        for (C1,C2,D) in self.connections:
            if (C1==city):
                actlist += [(C1,C2)]
            elif (C2==city):
               actlist += [(C2,C1)]
        return actlist 
    def result(self,city,action):
        (C1,C2) = action
        if C1==city:
            return C2
    def cost(self, city, action):
        
        (C1, C2) = action
        
        for conn in self.connections:
            if (conn[0] == C1 and conn[1] == C2 or conn[0]==C2 and conn[1]==C1): #verifica se ação existe 
                return conn[2] #se existir devolve o terceiro elemento do tuplo, o custo
        return None
            
    def heuristic(self, city, goal_city):
        
        (cx, cy) = self.coordinates[city]
        (gx, gy) = self.coordinates[goal_city]

        x, y = abs(cx-gx), abs(cy - gy)
        return hypot(x,y) #distância em linha reta entre a cidade atual e o objetivo. A heurística é admissível porque a linha reta é o caminho mais curto, todos os outros serão iguais ou maiores
                            #Ou seja, a solução encontrada com A* é sempre ótima
        #return 0

    def satisfies(self, city, goal_city):
        return goal_city==city


